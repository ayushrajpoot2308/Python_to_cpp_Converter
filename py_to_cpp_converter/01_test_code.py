# Assignments and expressions
a = 10
b = 20
c = a + b
print("Sum is", c)

# If-Else Condition
if c > 25:
    print("Value is large")
else:
    print("Value is small")

# While Loop
i = 0
while i < 3:
    print("While loop index:", i)
    i = i + 1

# For loop with range
for j in range(5):
    print("Range loop value:", j)

# List declaration and iteration
numbers = [5, 15, 25]
for n in numbers:
    print("List item:", n)

# Final expression and print
total = c + i + j
print("Final total is", total)
